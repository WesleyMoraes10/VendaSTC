    private void btExcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btExcluirActionPerformed
        try {
            int linha = jtableFormaPagamento.getSelectedRow();
            int codigoFormaPagamento = (int) jtableFormaPagamento.getValueAt(linha, 0);
            String descricao = (String) jtableFormaPagamento.getValueAt(linha, 1);

            int resposta = JOptionPane.showConfirmDialog(this, "Deseja excluir a forma de pagamento " + codigoFormaPagamento + " ?", "Confirmação",
                    JOptionPane.YES_NO_OPTION);
            if (resposta == JOptionPane.YES_OPTION) {

                if (controllerFormaPagamento.excluirFormaPagamentoController(codigoFormaPagamento)) {
                    //JOptionPane.showMessageDialog(this, "Produto excluido com sucesso!");
                    this.carregarFormasPagamento();
                } else {
                    JOptionPane.showMessageDialog(this, "Erro ao excluir produto!", "ERRO", JOptionPane.ERROR_MESSAGE);
                }
            }

        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Código inválido ou nenhum registro selecionado!", "ERRO", JOptionPane.ERROR_MESSAGE);
        }


    }//GEN-LAST:event_btExcluirActionPerformed

    private void btAlterarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btAlterarActionPerformed
        salvarAlterar = "alterar";
        int linha = jtableFormaPagamento.getSelectedRow();
        btExcluir.setEnabled(false);
        btNovo.setEnabled(false);

        try {
            int codigoFormaPagamento = (int) this.jtableFormaPagamento.getValueAt(linha, 0);
            //recuperar dados do banco
            modelFormaPagamento = controllerFormaPagamento.retornaFormaPagamentoController(codigoFormaPagamento);
            //setar na interface
            this.jtfDescricao.setText(String.valueOf(modelFormaPagamento.getFop_descricao()));
            habilitarDesabilitarCampo(true);

        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Código inválido ou nenhum registro selecionado!", "ERRO", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_btAlterarActionPerformed

    private void btCancelarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btCancelarActionPerformed
        habilitarDesabilitarCampo(false);
        limparCampos();
        btNovo.setEnabled(true);
        btExcluir.setEnabled(true);
    }//GEN-LAST:event_btCancelarActionPerformed

    private void btSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btSalvarActionPerformed
        if (salvarAlterar.equals("salvar")) {
            this.salvarFormaPagamento();
        } else if (salvarAlterar.equals("alterar")) {
            this.alterarFormapagamento();
        }
    }//GEN-LAST:event_btSalvarActionPerformed


