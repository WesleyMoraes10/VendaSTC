    private void habilitarDesabilitarCampo(boolean condicao) {
        jtfDescricao.setEnabled(condicao);
        btSalvar.setEnabled(condicao);

    }

    private void limparCampos() {
        jtfDescricao.setText("");
    }

    public void carregarFormasPagamento() {
        listaModelFormaPagamento = controllerFormaPagamento.retornaListaFormaPagamentoController();
        DefaultTableModel modelo = (DefaultTableModel) jtableFormaPagamento.getModel();
        modelo.setNumRows(0);

        int cont = listaModelFormaPagamento.size();
        for (int i = 0; i < cont; i++) {
            modelo.addRow(new Object[]{
                listaModelFormaPagamento.get(i).getFop_cod(),
                listaModelFormaPagamento.get(i).getFop_descricao()
            });
        }
    }

    public void salvarFormaPagamento() {
        modelFormaPagamento.setFop_descricao(jtfDescricao.getText().trim().toUpperCase());
        if (controllerFormaPagamento.salvarFormaPagamentoController(modelFormaPagamento) > 0) {
            JOptionPane.showMessageDialog(this, "Forma de Pagamento cadastrado com sucesso!", "ATENÇÃO", JOptionPane.INFORMATION_MESSAGE);
            limparCampos();
            habilitarDesabilitarCampo(false);
            this.carregarFormasPagamento();
        } else {
            JOptionPane.showMessageDialog(this, "Erro ao cadastrar forma de pagamento!", "ERRO", JOptionPane.ERROR_MESSAGE);
        }
    }
    
    public void alterarFormapagamento(){
        modelFormaPagamento.setFop_descricao(jtfDescricao.getText().trim().toUpperCase());
        if(controllerFormaPagamento.alterarFormaPagamentoController(modelFormaPagamento)){
            JOptionPane.showMessageDialog(this, "Forma de Pagamento alterado com sucesso!", "ATENÇÃO", JOptionPane.INFORMATION_MESSAGE);
            limparCampos();
            habilitarDesabilitarCampo(false);
            this.carregarFormasPagamento();
        }else{
            JOptionPane.showMessageDialog(this, "Erro ao alterar forma de pagamento!", "ERRO", JOptionPane.ERROR_MESSAGE);
        }
        
    }
    
    

