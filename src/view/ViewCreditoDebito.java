    private void btExcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btExcluirActionPerformed
        try {
            int linha = tblCreditoDebito.getSelectedRow();
            int codigoCreditoDebito = (int) tblCreditoDebito.getValueAt(linha, 0);
            String descricao = (String) tblCreditoDebito.getValueAt(linha, 1);

            int resposta = JOptionPane.showConfirmDialog(this, "Deseja excluir " + descricao + " ?", "Confirmação",
                    JOptionPane.YES_NO_OPTION);
            if (resposta == JOptionPane.YES_OPTION) {

                if (controllerCreditoDebito.excluirCreditoDebitoController(codigoCreditoDebito)) {
                    //JOptionPane.showMessageDialog(this, "Produto excluido com sucesso!");
                    this.retornaPorData();
                    this.retornaValorTotalCreditoComData();
                    this.retornaValorTotalDebitoComData();
                } else {
                    JOptionPane.showMessageDialog(this, "Erro ao excluir!", "ERRO", JOptionPane.ERROR_MESSAGE);
                }
            }

        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Código inválido ou nenhum registro selecionado!", "ERRO", JOptionPane.ERROR_MESSAGE);
        }

    }//GEN-LAST:event_btExcluirActionPerformed

