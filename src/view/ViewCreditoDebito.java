    private void btAlterarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btAlterarActionPerformed
        salvarAlterar = "alterar";
        int linha = tblCreditoDebito.getSelectedRow();
        btExcluir.setEnabled(false);
        btNovo.setEnabled(false);

        try {
            int codigoCreditoDebito = (int) this.tblCreditoDebito.getValueAt(linha, 0);
            //recuperar dados do banco
            modelCreditoDebito = controllerCreditoDebito.retornaCreditoDebitoController(codigoCreditoDebito);
            //setar na interface 
            this.jtfDescricao.setText(modelCreditoDebito.getCre_descricao());
            this.jtfValor.setText(String.valueOf(modelCreditoDebito.getCre_valor()));
            this.jcbFormaPaga.setSelectedItem(modelCreditoDebito.getCre_forma_paga());
            this.jcbTipo.setSelectedItem(modelCreditoDebito.getCre_tipo());
            habilitarDesabilitarCampo(true);

        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Código inválido ou nenhum registro selecionado!", "ERRO", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_btAlterarActionPerformed

